package charpter01;

public class Test07 {
    public static void main(String[] args) {
//      下面两个输出虽然一样，但是本质上还是有区别的，一个是数值，一个是字符串；
        System.out.println(100);
        System.out.println("100");

//        扩展：加减乘除的符号也是可以使用的
        System.out.println("----分割线----");
        System.out.println(100 + 10);
        System.out.println(100 - 10);
        System.out.println(100 * 10);
        System.out.println(100 / 10);

    }
}
