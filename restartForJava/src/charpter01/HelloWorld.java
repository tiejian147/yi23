package charpter01;

public class HelloWorld {
//    整个这一块的代码被称为main方法（程序的入口，sun公司java语言规范规定的）
//    也就是说：JVM在执行程序的时候，会主动去找这样的一个方法，没有这个规格的方法，程序是无法执行的
//    main方法也可以叫做主方法
//    注意：方法必须放在类体中，不能放在类体外面；
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("重新学习java，努力加油！");
    }
}
