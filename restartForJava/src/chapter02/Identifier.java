package chapter02;
//标识符：凡是程序员自己能命名的单词就都是标识符；
//可以标识：类名、方法名、接口名、变量名、常量名....
//main是属于一个方法的名称，但是这个方法名称不能改；因为这个main是SUN公司固定死的；
/*
5、标识符是有命名规则的：
   规则1、标识符只能由数字、字母、下划线、美元符号$组成，不能含有其他符号、不能有空格；
   规则2、标识符不能以数字开头；
   规则3、关键字不能做标识符；
   规则4、标识符严格区分大小写；
   规则5、理论上标识符不限制长度；

 */
public class Identifier {
    public static void main(String[] args) {
        System.out.println("test");
    }
}
